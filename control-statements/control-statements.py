# Make use of colons and indentation (white space)

temp = 18

# if
if temp == 18:
    print("18")

# if/else
if temp != 18:
    print("Not 18")
else:
    print("18")

# if/elif/else
if temp < 18:
    print("Less than 18")
elif temp == 18:
    print("Equal to 18")
else:
    print("Greater than 18")
